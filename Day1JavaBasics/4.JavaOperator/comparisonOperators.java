public class comparisonOperators {
    // Comparison operators: >, <, >=, <=, ==, used to compare values

    public static void main(String[] args) {
        int x = 8;
        int y = 4;
        int z = 4;

        System.out.println(x >y);
        System.out.println(y >= z);
        System.out.println(x < y);
        System.out.println(y <= z);
        System.out.println(z == x);
    }

}
